public class VariableManipulation {

    public static void main(String[]  args){

       int a=1;
       a++; //a=a+1
        System.out.println("value of a after a++ "+a  );
        a--; //a=a-1
        System.out.println("value of a after a__" +a );

        int b=3;
        int c=2;

        int sum=b+c;

        System.out.println("SUM IS"   +sum);

        int d=3;
        int e=2;

        int subtract = d-e;

        System.out.println("SUBTRACT   IS"  +subtract);

        int multiply = d * e;
        System.out.println("multiply   IS"  +multiply);

        int divide = d/e;
        System.out.println("divide is"  +divide);





    }



}
